            <h1>Karate Do.</h1>
            <hr>
            <br>
            <p>El karate es un arte marcial que tiene su origen en Okinawa. El karate es denominado: "el camino de la mano vacía". A la persona que practica este arte se la llama karateca.
            El objeto del Karate es diferente al de otras artes marciales: Mientras que en la mayoría de ellas, lo que se pretende es derrotar al mayor número de enemigos posible, la práctica del Karate busca ante todo el dominio de las técnicas de autodefensa.</p>
            <img src="imagenes/historia1.jpg" alt="">
            <p>Para explicar breve y claramente cuál es la idea se ha encontrado este párrafo: "Vengo hacia ti con las manos vacías. No tengo armas, pero, si soy obligado a defenderme, a defender mis principios o mi honor, si es cuestión de vida o muerte, de derecho o de injusticia, entonces aquí están mis armas: las manos vacías".</p>
            <p>Además del dominio de las técnicas y el ideal pacifista, es decir, rechaza cualquier forma de violencia, nunca atacar, pero si te atacan, saber defenderse, el Karate se involucra en una filosofía que proviene directamente del Budismo Zen.</p>
            <p>El Karate tiene su origen en China y la India, hacia el año 3.000 antes de Cristo. Desde el siglo seis de nuestra era las técnicas chinas se practican en Okinawa junto a técnicas locales. En el siglo quince, el rey Shoshin centraliza el poder en Shuri (Okinawa) y prohíbe el uso de las armas a la gente del pueblo.</p>
            <img src="imagenes/historia2.jpg" alt="">
            <p>En sus comienzos, el Karate se llamó simplemente "Te" (Mano). Cuando a finales del siglo diecinueve se incorporó su práctica como parte de los programas de educación física en las escuelas de Okinawa, y debido a su popularidad creciente dentro del Japón, se le renombró como "Karate" (Mano Vacía), para distinguirlo del "Tode", una forma de Mano originaria de China.</p>
            <p>En 1929 se celebró en Kyoto una convención nacional sobre artes marciales, y el alumno más avanzado de Higaonna, el Maestro Chojun Miyagi, fue invitado como representante de los estilos de Okinawa. No obstante, Miyagi no pudo asistir, y en su lugar fue uno de sus pupilos, llamado Shinsato.</p>
            <img src="imagenes/historia3.jpg" alt="">
            <p>Cuando a Shinsato le preguntaron por el nombre de su estilo, y dándose cuenta de que admitir que no tenía nombre formal podría dañar la reputación de la escuela, contestó que "Hanko-Ryu" ("Escuela Semi-dura"). A su regreso a Okinawa, discutió el asunto con el Maestro Miyagi y éste, admitiendo la necesidad de bautizar formalmente a la escuela, tomó un verso del poema chino "Ken No Taiyo Hakku": "Ho wa Goju o Tondosu" ("Todo en el Universo respira duro y suave"), y el Naha-Te tomó formalmente el nombre de Goju-Ryu (escuela dura-suave).</p>
            <img src="imagenes/hiatoria4.jpeg" alt="">
            <p>Goju tiene aplicación tanto en Karate como en la sociedad. Toda dureza o toda suavidad da lugar a una incapacidad para manejarse efectivamente en la vida. Las relaciones sociales son un pequeño ejemplo de esto: el uso de la cortesía puede suavizar un trato que de otra forma sería difícil. Por otro lado, en ciertas situaciones puede ser conveniente adoptar una actitud enérgica.</p>
            <p>También en Karate, dureza y suavidad combinadas llevan a la victoria. Esta existencia entremezclada de aspectos duros y aspectos suaves es lo que caracteriza al Karate Goju-Ryu.</p>
            <p>La extensión del Karate como arte marcial por todo el mundo, y las variaciones de estilos y escuelas, hace imposible dar una descripción detallada de las mismas.</p>
            <p>La existencia de cinturones varía de unos estilos a otros, pero por lo general suelen ser reconocidos: los llamados grados Kyu o cinturones de nivel inferior, e intermedio y los Dan o cinturones negros superiores. No obstante, los cinturones de colores también pueden variar según las escuelas, ya que en algunas escuelas alteran el orden de los colores, o quitan alguno de los colores.</p>
